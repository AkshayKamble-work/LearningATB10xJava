package JavaLoops;

public class ForLoop3 {

	public static void main(String[] args) {

		//WAP to print 5 to 1 numbers
		
		//Starting number = 5
		//last number = 1
		//dec by 1
		
		for(int j=5;  j>=1 ; j--) { 
			 //j=5   5>=1  true
			 //j=4   4>=1  true
			 //j=3   3>=1  true
			 //j=2   2>=1  true
			 //j=1   1>=1  true
			 //j=0   0>=1  false
			
			System.out.println(j);   //5 4 3 2 1
			
		}
		
		System.out.println("Hi");
	}

}
