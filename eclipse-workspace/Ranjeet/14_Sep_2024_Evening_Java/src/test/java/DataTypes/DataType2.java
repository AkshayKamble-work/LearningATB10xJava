package DataTypes;

public class DataType2 {

	public static void main(String[] args) {

		
		//Int data type -Size--> 4 Byte —-> 32 Bit —> (-2147483648 to 2147483647)
		
		//Syntax - DataType variableName = variableValue;
		
		
		int Number1 = 127;
		System.out.println("Int data Type -->"+ Number1);
		
		int Number2 = 214748364;
		System.out.println("Max size of integer data Type -->"+Number2);
		
		//int Number3 = 844883355;   -- Not allowed
		
		
		int No1 = -129449;
		System.out.println(No1);
		
		int N2 = -256483648;
		System.out.println(N2);
		
		System.out.println("Min size of integer data Type -->"+N2);
		
		
		//long data type -Size--> 8 Byte —-> 64 Bit —> (-9223372036854775808 to 9223372036854775807)
		
		//Syntax - DataType variableName = variableValue;

		long L1 = 348484l;
		long L2 = 8378564321l;
		long L3 = 922332356456785807l;
		long L4 = -9223372036854775808l;

		
		System.out.println("Long Data Type -->" +L1 +" + "+L2+" + "+L3);
		
		
		
		
	}
	
	

}
