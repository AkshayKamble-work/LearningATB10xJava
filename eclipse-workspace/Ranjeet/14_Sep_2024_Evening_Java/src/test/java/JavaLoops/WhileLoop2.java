package JavaLoops;

public class WhileLoop2 {

	public static void main(String[] args) {

		// WAP to print 5 to 10 numbers

//		starting number = 5
//	    ending number  =  10
//	    inc by 1

		int num = 5;

		while (num <= 10) { // true false

			num++; // 6

			System.out.println(num);

		}

		System.out.println("PC");
	}

}
