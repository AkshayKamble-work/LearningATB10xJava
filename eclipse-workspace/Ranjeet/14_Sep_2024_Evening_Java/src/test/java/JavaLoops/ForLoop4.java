package JavaLoops;

public class ForLoop4 {

	public static void main(String[] args) {

		// WAP to print 99 to 44 numbers

		// Starting number = 99
		// last number = 44
		// dec by 1

		for (int j = 99; j >= 44; j--) {
			// j=5 5>=1 true
			// j=4 4>=1 true
			// j=3 3>=1 true
			// j=2 2>=1 true
			// j=1 1>=1 true
			// j=0 0>=1 false

			System.out.println(j); // 5 4 3 2 1

		}

		System.out.println("Hi");
	}

}
