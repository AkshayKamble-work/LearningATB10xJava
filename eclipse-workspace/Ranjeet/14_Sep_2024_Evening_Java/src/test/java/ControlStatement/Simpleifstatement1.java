package ControlStatement;

public class Simpleifstatement1 {

	public static void main(String[] args) {

		int x = 10;
		int y = 20; // 30
		int z = x + y;

		if (z > 20) { // true
			System.out.println("x+y is greater than 20");
		}
		
		System.out.println();

		if (z < 10) {// falsd
			System.out.println("x+y is less than 10");
		}

		
		System.out.println("Test");
	}

}
