package Variables;

public class Demo1 {

	public static void main(String[] args) {

		// Variables declaration rules

		// 1. Must not begin with digit or number
		// 2. White space is not allowed
		// 3. Should not be keywords
		// 4. duplicate variable name not allowed

		// 1.Variable declaration (Allocating/Reserving memory)

		// Syntax --> DataType VariableName;

		String FirstName; // Collection of chara.
		

		int RollNumber;
		

		// 2.Variable Initialization (Assigning or inserting value)

		// Synatx -- VariableName = Variable value;

		FirstName = "Akash";
		RollNumber = 15;

		// 3.Usage

		System.out.println(FirstName); // Ketan
		System.out.println(RollNumber); // 15

	}
}
