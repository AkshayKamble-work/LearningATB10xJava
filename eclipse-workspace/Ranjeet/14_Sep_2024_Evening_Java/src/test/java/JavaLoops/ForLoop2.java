package JavaLoops;

public class ForLoop2 {

	public static void main(String[] args) {

		//WAP to print 50 to 70  numbers  = 51 52 53
		
//		System.out.println(50);
		
		
		//starting value = 50
		//last  value  = 70
		// increment by 1
		
		for(int num = 500 ; num<=1000000 ; num++) {  // num = 50   50<=70 - True
			                                   //51<=70  - true
			    
			                                  //70<=70  - true
			                                  //71<=70  - false
		    
			System.out.println(num);           //50  --> 51
			
			
		}
		
		System.out.println("PC");

		
	}

}
